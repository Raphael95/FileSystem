package domain;

public class Institude {
	String instId;
	String instName;
	public Institude()
	{
		
	}
	public Institude(String instName)
	{
		this.instName=instName;
	}
	public String getInstId() {
		return instId;
	}
	public void setInstId(String instId) {
		this.instId = instId;
	}
	public String getInstName() {
		return instName;
	}
	public void setInstName(String instName) {
		this.instName = instName;
	}
	

}
